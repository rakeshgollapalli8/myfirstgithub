# Arithmetic Operators
# These operators are used to perform mathematical operations like addition, subtraction, multiplication, etc.
a = 10
b = 5
print("a + b =", a + b)  # Addition
print("a - b =", a - b)  # Subtraction
print("a * b =", a * b)  # Multiplication
print("a / b =", a / b)  # Division
print("a % b =", a % b)  # Modulus
print("a ** b =", a ** b)  # Exponentiation
print("a // b =", a // b)  # Floor Division ----> REMAINDER
# Relational Operators
# These operators are used to compare two values.
x = 10
y = 20
print("x == y:", x == y)  # Equal to
print("x != y:", x != y)  # Not equal to
print("x > y:", x > y)    # Greater than
print("x < y:", x < y)    # Less than   
print("x >= y:", x >= y)  # Greater than or equal to
print("x <= y:", x <= y)  # Less than or equal to
# Logical Operators
# These operators are used to combine conditional statements.
p = True
q = False
print("p and q:", p and q)  # Logical AND
print("p or q:", p or q)    # Logical OR
print("not p:", not p)       # Logical NOT
# Assignment Operators
# These operators are used to assign values to variables.
m = 10
n = 20
m += n  # Equivalent to m = m + n
print("m after m += n:", m)
m -= n  # Equivalent to m = m - n
print("m after m -= n:", m)
m *= n  # Equivalent to m = m * n
print("m after m *= n:", m)
m /= n  # Equivalent to m = m / n
print("m after m /= n:", m)
m %= n  # Equivalent to m = m % n
print("m after m %= n:", m)
m **= n  # Equivalent to m = m ** n
print("m after m **= n:", m)
m //= n  # Equivalent to m = m // n
print("m after m //= n:", m)
# Bitwise Operators
# These operators are used to perform bit-level operations on integers.
a = 10  # In binary: 1010
b = 4   # In binary: 0100
print("a & b =", a & b)  # Bitwise AND
print("a | b =", a | b)  # Bitwise OR
print("a ^ b =", a ^ b)  # Bitwise XOR
print("~a =", ~a)        # Bitwise NOT
print("a << 1 =", a << 1)  # Left Shift
print("a >> 1 =", a >> 1)  # Right Shift
# Membership Operators
# These operators are used to test if a sequence contains a certain value.
my_list = [1, 2, 3, 4, 5]
print("3 in my_list:", 3 in my_list)      # Membership test
print("6 not in my_list:", 6 not in my_list)  # Membership test
# Identity Operators
# These operators are used to compare the memory locations of two objects.
x = ["apple", "banana"]
y = ["apple", "banana"]
z = x
print("x is y:", x is y)    # Identity test
print("x is z:", x is z)    # Identity test
print("x is not y:", x is not y)  # Identity test
print("x is not z:", x is not z)  # Identity test
